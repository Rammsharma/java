public class Pattern_by_nested_loop {
    public static void main(String[] args)
    {
        int m = 6;
        int n = 4;
        for(int i=1;i<=n;i++)
        {
            for(int j=1;j<=m;j++)
            {
                System.out.print("*");
            }

            System.out.println();

        }
    }
}
